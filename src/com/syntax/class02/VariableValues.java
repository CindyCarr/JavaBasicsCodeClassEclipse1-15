package com.syntax.class02;

public class VariableValues {

	public static void main(String[] args) {
		 //1st wat to create a variable
		int humidity=30;
		System.out.println(humidity);
				 
		//2nd way to create a variable
		int sum;
		sum=100;
		System.out.println(sum);
		
		//reassign variable
		sum=200;
		System.out.println(sum);
		
		//Create 3 variables of int type
		int week1, week2, week3;
		week1=3;
		week2=5;
		week3=7;
		System.out.println(week1 + week2 + week3);
		
		
		
		
	}

}
