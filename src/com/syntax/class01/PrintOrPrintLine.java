package com.syntax.class01;

public class PrintOrPrintLine {

	public static void main(String[] args) {
		// See the difference btwn Println vs Print
		
		// Println
		System.out.println("Ex 1 - Println");
		
		
		System.out.println("Hello");
		
		System.out.println("How are you");
		
		System.out.println("I am learning coding");
		
		// Print
		

		System.out.println(" ");
		
		System.out.println("Ex 2 - Print");
		
		System.out.print("Hello");
		
		System.out.print("How are you");
		
		System.out.print("I am learning coding");
		
		System.out.println(" ");
		
		// Combination of Print & Println
		
		System.out.println(" ");
		
		System.out.println("Ex. 3 - Print & Println");
		
		System.out.print("Hello");
		
		System.out.println("How are you");
		
		System.out.println("I am learning coding");

	}

}
