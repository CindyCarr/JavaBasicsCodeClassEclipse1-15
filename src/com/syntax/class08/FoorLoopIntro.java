package com.syntax.class08;

public class FoorLoopIntro {

	public static void main(String[] args) {
		// I want to say GM 5 times
		
		for (int i=1; i<=5; i++) {
			System.out.println("Good Morning"); //5 times
		}

		System.out.println(" ");

	
		
		
		
		for (int i=1; i>=5; i++) {
		System.out.print("How are you?"); //0 times - because 0 isn't greater than 5
	}
	
		System.out.println(" ");
	 
		
		
		
		for ( int i=1; i<=5; i--) {
			System.out.print(" infinite "); //infinite
		}
		
		
		
		
	}

}
