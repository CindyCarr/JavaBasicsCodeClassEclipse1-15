package com.syntax.class08;

public class NestedLoopIntro {

	public static void main(String[] args) {


		for (int i=1; i<=3; i++) {		//outer loop
			System.out.print("Hello ");
			
			for (int y=1; y<=2; y++) {	//nested loop
				System.out.print("Bye");
				
				System.out.println(" ");
				System.out.println(" ");
			}
		}
		
		
		
		
		

	}

}
