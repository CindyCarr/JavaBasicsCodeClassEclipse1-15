package com.syntax.class07;

public class IncrementAndDecrement {

	public static void main(String[] args) {

int x=10;


System.out.println(x); //prints 10 (current value)
System.out.println("---------------------------------");


x=x+1;
System.out.println(x); //prints 11 (adds 1)
System.out.println("---------------------------------");


x+=1;
System.out.println(x);//prints 12 (adds 1)
System.out.println("---------------------------------");


x++; // prints 13 (adds 1)
System.out.println(x);
System.out.println("---------------------------------");

x--; // prints 12 (subtracts 1)
System.out.println(x); //12

//increment and decrement operators work only with variables


	}

}
